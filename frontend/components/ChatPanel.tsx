// components/ChatPanel.tsx
import { forwardRef, useState } from "react";

interface ChatMessage {
  id: string;
  message: string;
  from: any;
  timestamp: Date;
  type: string;
}

interface ChatPanelProps {
  messages: ChatMessage[];
  onSendMessage: (message: string) => void;
   onClose?: () => void;
}

const ChatPanel = forwardRef<HTMLDivElement, ChatPanelProps>(({ messages, onSendMessage, onClose  }, ref) => {
  const [message, setMessage] = useState("");

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (message.trim()) {
      onSendMessage(message);
      setMessage("");
    }
  };

  return (
    <div className="h-full flex flex-col">

       {/* Header with close button for mobile */}
      <div className="lg:hidden flex items-center justify-between p-4 border-b border-gray-700">
        <h2 className="text-lg font-semibold">Chat</h2>
        <button
          onClick={onClose}
          className="p-2 rounded-lg hover:bg-gray-700 transition-colors"
          aria-label="Close chat"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>
      </div>
      
      <div ref={ref} className="flex-1 overflow-y-auto p-4 space-y-4">
        {messages.length === 0 ? (
          <div className="text-center text-gray-400 mt-8">
            No messages yet. Start the conversation!
          </div>
        ) : (
          messages.map((msg) => (
            <div key={msg.id} className="flex flex-col">
              <div className="flex justify-between items-baseline">
                <span className="font-medium text-blue-400">
                  {msg.from.firstName} {msg.from.lastName}
                </span>
                <span className="text-xs text-gray-400">
                  {new Date(msg.timestamp).toLocaleTimeString()}
                </span>
              </div>
              <p className="text-sm text-gray-200">{msg.message}</p>
            </div>
          ))
        )}
      </div>
      
      <form onSubmit={handleSubmit} className="p-4 border-t border-gray-700">
        <div className="flex space-x-2">
          <input
            type="text"
            value={message}
            onChange={(e) => setMessage(e.target.value)}
            placeholder="Type a message..."
            className="flex-1 bg-gray-700 text-white rounded-lg px-4 py-2 focus:outline-none focus:ring-2 focus:ring-blue-500"
          />
          <button
            type="submit"
            disabled={!message.trim()}
            className="bg-blue-600 hover:bg-blue-700 disabled:opacity-50 disabled:cursor-not-allowed rounded-lg px-4 py-2 transition-colors"
          >
            Send
          </button>
        </div>
      </form>
    </div>
  );
});

ChatPanel.displayName = "ChatPanel";

export default ChatPanel;